"""ETH v4 - FRACTIONAL-EXPOSURE low-MDD portfolio (13 legs).

WHY A SEPARATE FILE (v4)
------------------------
* BTC parent, ETH v1, v2 and v3 are all frozen. We add a new file.
* v1/v2/v3 use the parent's execution model: collapse all legs into ONE net
  direction by majority vote and hold a SINGLE +/-100%% position. That binary
  sizing is the main reason their MDD could not go below ~16%%.

EXECUTION MODEL - FRACTIONAL EXPOSURE (the key difference)
---------------------------------------------------------
v4 keeps the parent's leg state machine but OVERRIDES ``_reconcile`` to size the
single position in proportion to vote CONVICTION rather than betting 100%% on
the majority:

    raw      = (long_legs - short_legs) / n_legs * SCALE
    target   = quantize(clip(raw, -MAX_EXP, +MAX_EXP), STEP)   # signed weight
    position = target  (via ctx.enter_long/short(entry_pct=|target|))

When the target exposure bucket changes the strategy closes and re-enters at the
new size (the backtest engine has no partial resize), so every resize / flip /
flatten is a trade. Average gross exposure is small (~0.14), which is
exactly why drawdowns are much smaller than the +/-100%% models.

SELECTION (scripts/_alpha_lab/_eth_v4_optimize.py)
--------------------------------------------------
Random-restart greedy over the candidate leg pool, evaluated under the REAL
fractional-exposure single-account model (commission charged as close+re-enter
on every bucket change). Objective: minimise MDD subject to >=1 trade/day and
every calendar year positive.

PERFORMANCE - fractional-exposure model, commission 0.0004 both sides
---------------------------------------------------------------------
# FULL 2022-01..2026-06 : ret +48.4%  MDD 14.5%  +months 57.4%  worst -7.1%  ~1.58 trades/day
# HOLDOUT 2025-01..2026-05: ret +5.7%  MDD 14.5%  +months 47.1%  worst -5.2%
# Per-calendar-year (every year positive, incl. the 2022 and 2025 bears):
#   2022: ret +13.9%  MDD 13.6%  +months 67%  worst -7.1%
#   2023: ret +3.2%  MDD 10.8%  +months 67%  worst -3.8%
#   2024: ret +18.7%  MDD 14.1%  +months 50%  worst -3.9%
#   2025: ret +2.8%  MDD 14.4%  +months 50%  worst -5.2%
#   2026: ret +3.4%  MDD 14.3%  +months 50%  worst -3.7%
#
# vs v3 (~16%% MDD, ~0.4 trades/day): v4 has a LOWER MDD AND >=1 trade/day,
# thanks to conviction-proportional sizing. This is the smoother, lower-drawdown
# curve that the binary majority-vote models structurally could not produce.

RUNTIME CONTRACT
----------------
Reuses the parent lifecycle, data loaders, REST providers, signal funcs and
per-leg state machine. ``resolve_legs`` / ``_symbol_supported`` are scope-patched
during ``initialize`` (like v1/v2/v3); ``_reconcile`` is overridden on the
subclass so the parent and v1/v2/v3 are unaffected.
"""
