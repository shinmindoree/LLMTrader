def resolve_legs(symbol: str) -> list[dict[str, Any]]:
    sym = symbol.upper()
    if sym != BASELINE_SYMBOL:
        raise ValueError(
            f"MultiFactorPortfolioEthV4Strategy: symbol {sym} is not "
            f"supported; this strategy is tuned exclusively for "
            f"{BASELINE_SYMBOL}."
        )
    return ETH_V4_LEGS


def _symbol_supported(symbol: str) -> bool:
    return symbol.upper() == BASELINE_SYMBOL


def _target_exposure(long_count: int, short_count: int, n_legs: int) -> float:
    """Conviction-proportional signed exposure, quantized to STEP buckets."""
    if n_legs <= 0:
        return 0.0
    raw = (long_count - short_count) / n_legs * EXPOSURE_SCALE
    raw = max(-EXPOSURE_MAX, min(EXPOSURE_MAX, raw))
    if EXPOSURE_STEP > 0:
        raw = round(raw / EXPOSURE_STEP) * EXPOSURE_STEP
    return float(raw)


