class MultiFactorPortfolioEthV4Strategy(MultiFactorPortfolioStrategy):
    """ETH v4 - 13-leg fractional-exposure low-MDD multi-factor portfolio.

    Identical lifecycle to the parent, but ``_reconcile`` sizes the single
    position in proportion to vote conviction (fractional exposure) instead of
    the parent's +/-100%% majority vote, which lowers drawdown while keeping
    >=1 trade/day.
    """

    def initialize(self, ctx: StrategyContext) -> None:
        ctx_symbol = getattr(ctx, "symbol", None)
        if ctx_symbol and str(ctx_symbol).upper() != BASELINE_SYMBOL:
            raise ValueError(
                f"MultiFactorPortfolioEthV4Strategy: ctx.symbol={ctx_symbol} "
                f"is not supported; tuned exclusively for {BASELINE_SYMBOL}."
            )
        self._committed_exposure: float = 0.0
        orig_resolve = _mfp.resolve_legs
        orig_supported = _mfp._symbol_supported
        _mfp.resolve_legs = resolve_legs
        _mfp._symbol_supported = _symbol_supported
        try:
            super().initialize(ctx)
        finally:
            _mfp.resolve_legs = orig_resolve
            _mfp._symbol_supported = orig_supported

    def _reconcile(self, ctx: StrategyContext, target: int, long_count: int,
                   short_count: int, ts: int) -> None:
        """Override: size the single position by fractional exposure.

        Closes and re-enters whenever the quantized target exposure bucket
        changes (the backtest engine cannot partially resize). ``target`` (the
        parent's net sign) is ignored in favour of the conviction-weighted
        exposure.
        """
        n_legs = len(self._legs)
        target_exp = _target_exposure(long_count, short_count, n_legs)

        # Sync committed exposure with reality (external close, SL, etc.).
        pos = getattr(ctx, "position", None)
        cur_size = 0.0
        if pos is not None:
            try:
                cur_size = float(getattr(pos, "size", 0.0) or 0.0)
            except (TypeError, ValueError):
                cur_size = 0.0
        if abs(cur_size) < 1e-12:
            self._committed_exposure = 0.0

        if abs(target_exp - self._committed_exposure) < 1e-9:
            return  # same bucket - hold

        # Close any existing position, then re-enter at the new exposure.
        if abs(cur_size) > 1e-12:
            try:  # noqa: SIM105
                ctx.close_position(
                    reason=f"MFP-v4 resize {self._committed_exposure:+.2f}"
                           f"->{target_exp:+.2f}")
            except Exception:  # noqa: BLE001, S110
                pass
        mag = abs(target_exp)
        if target_exp > 1e-9:
            ctx.enter_long(
                reason=f"MFP-v4 long exp={mag:.2f} "
                       f"({long_count}L/{short_count}S)", entry_pct=mag)
            self._committed_side = 1
        elif target_exp < -1e-9:
            ctx.enter_short(
                reason=f"MFP-v4 short exp={mag:.2f} "
                       f"({long_count}L/{short_count}S)", entry_pct=mag)
            self._committed_side = -1
        else:
            self._committed_side = 0
        self._committed_exposure = target_exp
